#include <unistd.h>

void	ft_putnbr(int nb)
{
	int		div;
	char	c;

	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (nb < 0)
		{
			nb *= -1;
			write(1, "-", 1);
		}
		div = 1;
		while ((nb / div) / 10)
			div *= 10;
		while (div)
		{
			c = nb / div + '0';
			write(1, &c, 1);
			nb %= div;
			div /= 10;
		}
	}
}
